public class WrapCls {
    public static void main(String[] args) {
        String a ="10";
        Integer x = Integer.valueOf(a);
        Float b = Float.valueOf(26.55f);
        Double c = 30.6543;
        Character d = 'c';
        Boolean e = Boolean.valueOf(true);
        System.out.println(x+"\n"+b+"\n"+c+"\n"+d+"\n"+e);
    }
}
//Java is not fully Object-Oriented , as it has primituve data types
//but can make it fully object oriented by using wrapper class.